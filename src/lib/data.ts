export const getPlacesByCategory = (categoryId: string) =>
  PLACES.filter((p) => p.category === categoryId);

export const getCategoryById = (id: string) => CATEGORIES.find((c) => c.id === id);

export const getPlaceById = (id: string) => PLACES.find((p) => p.id === id);

export const searchPlaces = (query: string) => {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return PLACES.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.tags.some((t) => t.toLowerCase().includes(q)) ||
      (getCategoryById(p.category)?.name.toLowerCase().includes(q) ?? false)
  );
};

